name = "peace0123456789"

print(name[0:2]) # goes from 0 to 2-1  just like range function n to n-1.
print(name[2:-1]) # same as name[2:4]


print(name[0:10:2]) # skip n-1 character Ex:- [start:stop:jumping]
print(name[0:10:1]) # in this not going to skip anything


print(name[:4]) # Replace the first empty number with 0
print(name[1:]) # Replace the second empty number with length 
print(name[:])